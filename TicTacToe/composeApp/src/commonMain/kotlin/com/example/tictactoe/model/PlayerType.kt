package com.example.tictactoe.model

/**
 * Represents the available player types in the Tic-Tac-Toe game.
 */
enum class PlayerType {
    /**
     * Human-controlled player type.
     */
    HUMAN,

    /**
     * Easy difficulty AI player type.
     */
    EASY_AI,

    /**
     * Medium difficulty AI player type.
     */
    MEDIUM_AI,

    /**
     * Hard difficulty AI player type.
     */
    HARD_AI
}

