package com.example.tictactoe.ui.theme

import androidx.compose.ui.graphics.Color

/**
 * Defines the color palette used throughout the Tic-Tac-Toe application.
 */
object TicTacToeColors {

    /**
     * Primary navy color used for app bars and key surfaces.
     */
    val NavyPrimary: Color = Color(0xFF0D1B2A)

    /**
     * Secondary amber accent color used for highlights.
     */
    val AmberSecondary: Color = Color(0xFFFFB703)

    /**
     * Off-white surface color used for surfaces such as cards and the board background.
     */
    val OffWhiteSurface: Color = Color(0xFFE0E1DD)

    /**
     * Teal tertiary color used for secondary highlights.
     */
    val TealTertiary: Color = Color(0xFF2A9D8F)

    /**
     * Color used to render X pieces on the board.
     */
    val XColor: Color = Color(0xFFF4A261)

    /**
     * Color used to render O pieces on the board.
     */
    val OColor: Color = Color(0xFF2A9D8F)

    /**
     * Background color applied to the main app surface.
     */
    val Background: Color = Color(0xFF1B263B)

    /**
     * Color used for content that appears on top of the primary color.
     */
    val OnPrimary: Color = Color(0xFFFDFDFD)

    /**
     * Color used for content that appears on top of surface colors.
     */
    val OnSurface: Color = Color(0xFF0D1B2A)
}

